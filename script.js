function createMatrix(n, m) {
    let arr = new Array(n);

    for (let i = 0; i < arr.length; i++) {
        arr[i] = new Array(m);
    }

    for (let i = 0; i < n; i++) {
        for (let j = 0; j < m; j++) {
            arr[i][j] = { b: 1, r: 1 };
        }
    }

    return arr;
}

function removeRows(arr, rows, n, m) {
    for (let i = 0; i < rows.length; i++) {
        for (let j = rows[i] - 1; j < rows[i]; j++) {
            for (let k = 0; k < m; k++) {
                let copy = {...arr[j][k]};
                copy.b = 0;
                arr[j][k] = copy;
            }
        }
    }
    
    return arr;
}

function removeCols(arr, cols, n, m) {
    for (let i = 0; i < cols.length; i++) {
        for (let j = 0; j < n; j++) {
            for (let k = cols[i] - 1; k < cols[i]; k++) {
                let copy = {...arr[j][k]};
                copy.r = 0;
                arr[j][k] = copy;
            }
        }
    }
    
    return arr;
}

function findBiggestRowHole(arr,n,m){
    let maxCounter = -1;
    
    for(let i = 0; i < n - 1; i++){
        let counter = 1;
        
        for(let j = i+1; j < n; j++){
            if(arr[j][0].b === 0){
                counter++;
            }
        }
        if(arr[i][0].b === 0){
            counter++;
        }
        if(counter > maxCounter){
            maxCounter = counter;
        }
    }
    
    return maxCounter;
}

function findBiggestColHole(arr,n,m){
    let maxCounter = -1;
    
    for(let i = 0; i < m - 1; i++){
        let counter = 1;
        
        for(let j = i+1; j < m; j++){
            if(arr[0][j].r === 0){
                counter++;
            }
        }
        if(arr[0][i].r === 0){
            counter++;
        }
        if(counter > maxCounter){
            maxCounter = counter;
        }
    }
    
    return maxCounter;
}

